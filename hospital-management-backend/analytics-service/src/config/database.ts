import { Pool, PoolConfig } from 'pg';
import dotenv from 'dotenv';

dotenv.config();

const poolConfig: PoolConfig = {
  host: process.env.DB_HOST || 'localhost',
  port: parseInt(process.env.DB_PORT || '5436'),
  database: process.env.DB_NAME || 'analytics_service_db',
  user: process.env.DB_USER || 'analytics_user',
  password: process.env.DB_PASSWORD || 'analytics_password_123',
  max: 20, // Maximum number of clients in the pool
  idleTimeoutMillis: 30000, // Close idle clients after 30 seconds
  connectionTimeoutMillis: 2000, // Return an error after 2 seconds if connection could not be established
};

export const pool = new Pool(poolConfig);

// Test database connection
export const testConnection = async (): Promise<boolean> => {
  try {
    const client = await pool.connect();
    await client.query('SELECT NOW()');
    client.release();
    console.log('âœ… Database connected successfully');
    return true;
  } catch (error) {
    console.error('âŒ Database connection failed:', error);
    return false;
  }
};

// Graceful shutdown
process.on('SIGINT', async () => {
  console.log('ðŸ”„ Closing database pool...');
  await pool.end();
  process.exit(0);
});

export default pool;
